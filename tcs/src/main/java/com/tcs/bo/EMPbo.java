package com.tcs.bo;

public class EMPbo {

	private Integer ENO;
	private String ENAME;
	private String DESCIGNATION;
	private Float BASICSAL;
	private Float GROSSSAL;
	private Float NETSAL;

	public Integer getENO() {
		return ENO;
	}

	public void setENO(Integer eNO) {
		ENO = eNO;
	}

	public String getENAME() {
		return ENAME;
	}

	public void setENAME(String eNAME) {
		ENAME = eNAME;
	}

	public String getDESCIGNATION() {
		return DESCIGNATION;
	}

	public void setDESCIGNATION(String dESCIGNATION) {
		DESCIGNATION = dESCIGNATION;
	}

	public Float getBASICSAL() {
		return BASICSAL;
	}

	public void setBASICSAL(Float bASICSAL) {
		BASICSAL = bASICSAL;
	}

	public Float getGROSSSAL() {
		return GROSSSAL;
	}

	public void setGROSSSAL(Float gROSSSAL) {
		GROSSSAL = gROSSSAL;
	}

	public Float getNETSAL() {
		return NETSAL;
	}

	public void setNETSAL(Float nETSAL) {
		NETSAL = nETSAL;
	}

}
